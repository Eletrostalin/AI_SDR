def generate_email_template_prompt(company_info: str, campaign_name: str, user_request: str) -> str:
    """
    Формирует промт для генерации шаблона письма с помощью OpenAI GPT.

    :param company_info: Информация о компании.
    :param campaign_name: Название кампании.
    :param user_request: Пожелания пользователя.
    :return: Сформированный текст промта.
    """
    return f"""
    Учитывая следующую информацию:
    - О компании: {company_info}
    - Название кампании: {campaign_name}
    - Пожелания пользователя: {user_request}

    Создай шаблон письма. Письмо должно быть профессиональным, лаконичным и соответствовать предоставленным пожеланиям.
    Пример письма:
    ---
    Добрый день!

    Мы рады сообщить, что в рамках кампании "{campaign_name}" доступны новые возможности. Подробнее вы можете узнать на нашем сайте.

    Благодарим за внимание!
    ---
    """